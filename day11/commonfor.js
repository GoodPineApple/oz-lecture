// 1부터 5까지 콘솔찍는 코드
for (let i = 1; i <= 5; i++) {
  console.log(i);
}
console.log("//////////////////");

// 1부터 10까지 홀수만 찍는 코드
for (let i = 1; i <= 10; i += 2) {
  console.log(i);
}
